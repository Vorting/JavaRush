package JavaSyntax.lvl4.lecture13;

public class ForLineDemo {
    public static void main(String[] args) {

        int i, j;
        for (i = 0; i < 10; i++) {
            System.out.print(8);
        }
        System.out.println();
        for (j = 0; j < 10; j++) {
            System.out.println("8");
        }
    }
}
